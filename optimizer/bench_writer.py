def write_report(filepath: str,
                  original_circuit,
                  optimized_gates: Gates,
                  optimized_cost: float,
                  optimizer_name: str = "Unknown",
                  time_seconds: float = 0.0,
                  mutation_stats: Optional[Dict] = None) -> str:
    """
    Writes a human-readable optimization report.

    Includes:
        - Original vs optimized PAC cost
        - Improvement percentage
        - Gate count change
        - Gate-level diff (what was added/removed/changed)
        - Functional equivalence status

    Args:
        filepath          : output file path
        original_circuit  : Circuit object (before optimization)
        optimized_gates   : gates dict after optimization
        optimized_cost    : PAC cost of optimized circuit
        optimizer_name    : which optimizer produced this result
        time_seconds      : how long optimization took
        mutation_stats    : from get_mutation_stats() — optional

    Returns:
        filepath (for chaining)
    """
    from optimizer.cost_function import compute_pac_cost
    from optimizer.mutations     import (get_mutation_stats,
                                          validate_functional_equivalence)

    out_dir = os.path.dirname(filepath)
    if out_dir:
        os.makedirs(out_dir, exist_ok=True)

    orig_cost   = original_circuit.cost
    improvement = ((orig_cost - optimized_cost) / orig_cost * 100) \
                   if orig_cost else 0.0

    # Detailed cost breakdown for both
    orig_detail = compute_pac_cost(original_circuit.gates,
                                    original_circuit.inputs)
    opt_detail  = compute_pac_cost(optimized_gates,
                                    original_circuit.inputs)

    # Gate-level diff
    if mutation_stats is None:
        mutation_stats = get_mutation_stats(original_circuit.gates,
                                             optimized_gates)

    # Functional equivalence check
    is_equivalent = validate_functional_equivalence(
        original_circuit.inputs,
        original_circuit.outputs,
        original_circuit.gates,
        optimized_gates
    )

    with open(filepath, 'w', encoding='utf-8') as f:
        _write_report_content(
            f, original_circuit, optimized_gates, optimized_cost,
            orig_cost, improvement, orig_detail, opt_detail,
            mutation_stats, optimizer_name, time_seconds, is_equivalent
        )

    return filepath


def _write_report_content(f, original_circuit, optimized_gates,
                           optimized_cost, orig_cost, improvement,
                           orig_detail, opt_detail, mutation_stats,
                           optimizer_name, time_seconds, is_equivalent):
    """Writes the report content to an open file handle."""

    sep  = "=" * 60
    dash = "-" * 60

    f.write(f"{sep}\n")
    f.write(f"  AI-DRIVEN CIRCUIT OPTIMIZATION REPORT\n")
    f.write(f"{sep}\n")
    f.write(f"  Circuit    : {original_circuit.name}\n")
    f.write(f"  Optimizer  : {optimizer_name}\n")
    f.write(f"  Time       : {round(time_seconds, 3)}s\n")
    f.write(f"  Generated  : "
            f"{datetime.now().strftime('%Y-%m-%d %H:%M:%S')}\n")
    f.write(f"{sep}\n\n")

    # ── Functional Correctness ────────────────────────────────
    f.write(f"  FUNCTIONAL CORRECTNESS\n")
    f.write(f"{dash}\n")
    if is_equivalent:
        status = "EQUIVALENT ✓  (safe to use)"
    else:
        status = "NOT EQUIVALENT ✗  (discard this result)"
    f.write(f"  Status     : {status}\n")
    f.write(f"  Method     : Truth-table exhaustive / sampled check\n\n")

    # ── PAC Cost Comparison ───────────────────────────────────
    f.write(f"  PAC COST COMPARISON\n")
    f.write(f"{dash}\n")
    f.write(f"  {'Metric':<20} {'Original':>12} "
            f"{'Optimized':>12} {'Change':>10}\n")
    f.write(f"  {'-'*20} {'-'*12} {'-'*12} {'-'*10}\n")

    metrics = [
        ('Power',      orig_detail['power'],     opt_detail['power']),
        ('Area',       orig_detail['area'],       opt_detail['area']),
        ('WireLength', orig_detail['wirelength'], opt_detail['wirelength']),
        ('TOTAL COST', orig_cost,                 optimized_cost),
    ]

    for label, orig_val, opt_val in metrics:
        change     = opt_val - orig_val
        change_pct = (change / orig_val * 100) if orig_val else 0
        arrow      = "↓" if change < 0 else ("↑" if change > 0 else "─")
        f.write(f"  {label:<20} {orig_val:>12.4f} {opt_val:>12.4f} "
                f"{arrow}{abs(change_pct):>8.2f}%\n")

    f.write(f"\n")
    f.write(f"  Weights    : α=0.4 (Power)  β=0.4 (Area)  "
            f"γ=0.2 (Wire)\n")
    f.write(f"  Net improvement : {round(improvement, 4)}%\n\n")

    # ── Gate Count Summary ────────────────────────────────────
    f.write(f"  GATE COUNT\n")
    f.write(f"{dash}\n")
    orig_count = mutation_stats['original_count']
    mut_count  = mutation_stats['mutated_count']
    net        = mutation_stats['net_gate_change']
    f.write(f"  Original   : {orig_count}\n")
    f.write(f"  Optimized  : {mut_count}\n")
    f.write(f"  Net change : {net:+d} gates\n\n")

    # ── Gate Type Breakdown ───────────────────────────────────
    orig_types = _count_gate_types(original_circuit.gates)
    opt_types  = _count_gate_types(optimized_gates)
    all_types  = sorted(set(orig_types) | set(opt_types))

    if all_types:
        f.write(f"  GATE TYPE BREAKDOWN\n")
        f.write(f"{dash}\n")
        f.write(f"  {'Type':<10} {'Original':>10} "
                f"{'Optimized':>10} {'Change':>8}\n")
        f.write(f"  {'-'*10} {'-'*10} {'-'*10} {'-'*8}\n")
        for gt in all_types:
            o = orig_types.get(gt, 0)
            m = opt_types.get(gt, 0)
            f.write(f"  {gt:<10} {o:>10} {m:>10} {m-o:>+8}\n")
        f.write(f"\n")

    # ── Gate-Level Diff ───────────────────────────────────────
    f.write(f"  GATE-LEVEL DIFF\n")
    f.write(f"{dash}\n")

    removed = mutation_stats['removed_signals']
    added   = mutation_stats['added_signals']
    changed = mutation_stats['changed_signals']

    if not removed and not added and not changed:
        f.write(f"  No structural changes (input reordering only)\n")
    else:
        if removed:
            f.write(f"  Removed ({len(removed)} gates):\n")
            for sig in removed[:20]:
                orig_gt, orig_gi = original_circuit.gates.get(
                    sig, ('?', []))
                f.write(f"    - {sig} = {orig_gt}"
                        f"({', '.join(orig_gi)})\n")
            if len(removed) > 20:
                f.write(f"    ... and {len(removed)-20} more\n")

        if added:
            f.write(f"\n  Added ({len(added)} gates):\n")
            for sig in added[:20]:
                opt_gt, opt_gi = optimized_gates.get(sig, ('?', []))
                f.write(f"    + {sig} = {opt_gt}"
                        f"({', '.join(opt_gi)})\n")
            if len(added) > 20:
                f.write(f"    ... and {len(added)-20} more\n")

        if changed:
            f.write(f"\n  Changed ({len(changed)} gates):\n")
            for sig in changed[:20]:
                orig_gt, orig_gi = original_circuit.gates.get(
                    sig, ('?', []))
                opt_gt,  opt_gi  = optimized_gates.get(
                    sig, ('?', []))
                f.write(f"    ~ {sig}: "
                        f"{orig_gt}({', '.join(orig_gi)}) "
                        f"→ {opt_gt}({', '.join(opt_gi)})\n")
            if len(changed) > 20:
                f.write(f"    ... and {len(changed)-20} more\n")

    f.write(f"\n")

    # ── ABC Verification Command ──────────────────────────────
    f.write(f"  VERIFICATION\n")
    f.write(f"{dash}\n")
    f.write(f"  To verify functional equivalence externally:\n\n")
    f.write(f"  ABC (recommended):\n")
    f.write(f"    abc -c \"cec {original_circuit.name}.bench "
            f"{original_circuit.name}_optimized.bench\"\n")
    f.write(f"    Expected: Networks are equivalent.\n\n")
    f.write(f"  Yosys:\n")
    f.write(f"    yosys -p \"read_bench {original_circuit.name}.bench; "
            f"read_bench {original_circuit.name}_optimized.bench; "
            f"equiv_check\"\n\n")
    f.write(f"{sep}\n")


def _count_gate_types(gates: Gates) -> Dict[str, int]:
    counts: Dict[str, int] = {}
    for _, (gt, _) in gates.items():
        counts[gt] = counts.get(gt, 0) + 1
    return counts


