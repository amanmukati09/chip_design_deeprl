def save_optimized_circuit(output_dir: str,
                            original_circuit,
                            optimized_gates: Gates,
                            optimized_cost: float,
                            optimizer_name: str = "SA",
                            time_seconds: float = 0.0) -> Tuple[str, str]:
    """
    Saves the optimized .bench file and the text report in one call.

    Args:
        output_dir        : directory to write files into
        original_circuit  : Circuit object (before optimization)
        optimized_gates   : gates dict after optimization
        optimized_cost    : PAC cost of optimized circuit
        optimizer_name    : label for the report
        time_seconds      : optimization runtime

    Returns:
        (bench_path, report_path)
    """
    from optimizer.mutations import get_mutation_stats

    name        = original_circuit.name
    bench_path  = os.path.join(output_dir, f"{name}_optimized.bench")
    report_path = os.path.join(output_dir, f"{name}_report.txt")

    mutation_stats = get_mutation_stats(original_circuit.gates,
                                        optimized_gates)

    write_bench(
        bench_path,
        original_circuit.inputs,
        original_circuit.outputs,
        optimized_gates,
        name=f"{name}_optimized"
    )

    write_report(
        report_path,
        original_circuit,
        optimized_gates,
        optimized_cost,
        optimizer_name  = optimizer_name,
        time_seconds    = time_seconds,
        mutation_stats  = mutation_stats
    )

    return bench_path, report_path


