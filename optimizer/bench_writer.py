def write_bench(filepath: str,
                inputs: List[str],
                outputs: List[str],
                gates: Gates,
                name: str = "optimized") -> str:
    """
    Writes a valid .bench netlist file.

    Format:
        # header comment
        INPUT(signal)
        OUTPUT(signal)
        signal = GATETYPE(in1, in2, ...)

    Args:
        filepath : output file path
        inputs   : primary input signal names
        outputs  : primary output signal names
        gates    : gates dict {out_sig: (gate_type, [inputs])}
        name     : circuit name for header comment

    Returns:
        filepath (for chaining)
    """
    out_dir = os.path.dirname(filepath)
    if out_dir:
        os.makedirs(out_dir, exist_ok=True)

    # Build topological order so signals are defined before use
    ordered = _topological_sort(inputs, gates)

    with open(filepath, 'w',  encoding='utf-8') as f:
        # Header
        f.write(f"# {name}\n")
        f.write(f"# Generated by AI-Driven Circuit Optimizer\n")
        f.write(f"# {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}\n")
        f.write(f"# Gates: {len(gates)}  "
                f"Inputs: {len(inputs)}  "
                f"Outputs: {len(outputs)}\n")
        f.write("#\n")

        # Primary inputs
        f.write("\n")
        for inp in inputs:
            f.write(f"INPUT({inp})\n")

        # Primary outputs
        f.write("\n")
        for out in outputs:
            f.write(f"OUTPUT({out})\n")

        # Gate definitions in topological order
        f.write("\n")
        for sig in ordered:
            if sig not in gates:
                continue
            gate_type, gate_inputs = gates[sig]
            inputs_str = ", ".join(gate_inputs)
            f.write(f"{sig} = {gate_type}({inputs_str})\n")

    return filepath


def _topological_sort(primary_inputs: List[str],
                       gates: Gates) -> List[str]:
    """
    Returns gate signal names in topological order
    (each gate appears after all its inputs are defined).
    """
    visited = set(primary_inputs)
    order   = []

    def visit(sig: str):
        if sig in visited or sig not in gates:
            return
        visited.add(sig)
        _, gate_inputs = gates[sig]
        for inp in gate_inputs:
            visit(inp)
        order.append(sig)

    for sig in gates:
        visit(sig)

    return order


